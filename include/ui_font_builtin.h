/*
 * ui_font_builtin_find(cp)
 * Returns slot index [0, GLYPH_COUNT) for Unicode codepoint cp.
 * Returns 0 (SPACE) for codepoints < 0x20 (control characters).
 * Returns -1 if the codepoint is not in the font.
 */
static inline int ui_font_builtin_find(uint32_t cp) {
    if (cp < 0x20u)
        return 0; /* control → space */
    if (cp <= 0x7Eu)
        return (int)(cp - 0x20u);
    if (cp >= 0x2500u && cp <= 0x257Fu)
        return (int)(95u + (cp - 0x2500u));
    return -1;
}

/*
 * ui_font_builtin_uv(idx, u0, v0, u1, v1)
 * Writes the normalised [0,1] UV rectangle for glyph slot idx.
 * idx must be in [0, UI_FONT_BUILTIN_GLYPH_COUNT).
 */
static inline void ui_font_builtin_uv(int idx, float *u0, float *v0, float *u1, float *v1) {
    const int TOTAL_ROWS = (223 + 16 - 1) / 16; /* 14 */
    int col = idx % 16;
    int row = idx / 16;
    *u0 = (float)col / (float)16;
    *u1 = (float)(col + 1) / (float)16;
    *v0 = (float)row / (float)TOTAL_ROWS;
    *v1 = (float)(row + 1) / (float)TOTAL_ROWS;
}

/*
 * ui_font_builtin_render_atlas(buf)
 *
 * Expands the 1-bit bitmap data into a flat R8 pixel buffer of
 * UI_FONT_BUILTIN_ATLAS_W * UI_FONT_BUILTIN_ATLAS_H = 28672 bytes.
 * Each pixel is either 0x00 (off) or 0xFF (on).
 *
 * Call once at startup; upload the result as an R8 (or RGBA8-expanded)
 * GPU texture.  buf must be at least ATLAS_W * ATLAS_H bytes.
 */
static inline void ui_font_builtin_render_atlas(uint8_t *buf) {
    const int W = UI_FONT_BUILTIN_ATLAS_W;    /* 128 */
    const int GW = UI_FONT_BUILTIN_GLYPH_W;   /* 8 */
    const int GH = UI_FONT_BUILTIN_GLYPH_H;   /* 16 */
    const int C = UI_FONT_BUILTIN_ATLAS_COLS; /* 16 */
    for (int slot = 0; slot < UI_FONT_BUILTIN_GLYPH_COUNT; ++slot) {
        int gx = (slot % C) * GW;
        int gy = (slot / C) * GH;
        for (int r = 0; r < GH; ++r) {
            uint8_t bits = ui_font_builtin_bitmaps[slot][r];
            for (int px = 0; px < GW; ++px) {
                int on = (bits >> (GW - 1 - px)) & 1;
                buf[(gy + r) * W + (gx + px)] = on ? 0xFFu : 0x00u;
            }
        }
    }
}

